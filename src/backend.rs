use arrow::array::{AsArray, Float32Array};
use arrow::datatypes::Float32Type;
use arrow::record_batch::RecordBatch;

use crate::config::{BackendConfig, BenchConfig};
pub use crate::stats::disk::DiskUsage;
pub use crate::stats::mem::MemoryUsage;

/// A pre-processed batch of test data ready for backend ingestion.
pub struct Batch {
    /// The raw Arrow RecordBatch (for backends that need direct Arrow access).
    pub inner: RecordBatch,
    /// Key values extracted from column 0.
    pub keys: Vec<String>,
    /// Value column names (col_0, col_1, ...).
    pub columns: Vec<String>,
}

impl Batch {
    /// Returns the Float32 value columns (all columns except the key column).
    pub fn value_columns(&self) -> Vec<&Float32Array> {
        (1..self.inner.num_columns())
            .map(|i| self.inner.column(i).as_primitive::<Float32Type>())
            .collect()
    }
}

/// A benchmarkable storage backend.
///
/// Not object-safe — used via generics (`Bench::run::<B>`).
/// `Clone` is required because Criterion's async closure clones the handle per iteration.
pub trait Backend: Sized + Send + Sync + Clone {
    type Config: BackendConfig;
    type Response: Send + 'static;

    /// Start containers, create tables, prepare connections.
    fn init(config: &BenchConfig<Self::Config>) -> impl Future<Output = Self> + Send;

    /// Write a single batch of test data (already sized to write_batch_size).
    fn write_batch(&self, batch: &Batch) -> impl Future<Output = ()> + Send;

    /// Execute a single read — the hot path measured by Criterion.
    /// keys and columns are generated by the orchestrator.
    fn read(&self, keys: &[String], columns: &[String]) -> impl Future<Output = Self::Response> + Send;

    /// Report current memory usage (RSS, shared, virtual).
    fn memory_usage(&self) -> impl Future<Output = MemoryUsage> + Send;

    /// Report current disk usage.
    fn disk_usage(&self) -> impl Future<Output = DiskUsage> + Send;

    /// Flush all buffered writes to durable storage.
    /// Called after all write_batch calls, before disk_usage is measured.
    fn flush(&self) -> impl Future<Output = ()> + Send {
        async {}
    }

    /// Stop containers, close connections.
    fn cleanup(self) -> impl Future<Output = ()> + Send;
}
